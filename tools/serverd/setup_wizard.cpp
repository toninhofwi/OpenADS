std::string trim(const std::string& s) {
    std::size_t b = 0, e = s.size();
    while (b < e && std::isspace(static_cast<unsigned char>(s[b]))) ++b;
    while (e > b && std::isspace(static_cast<unsigned char>(s[e - 1]))) --e;
    return s.substr(b, e - b);
}

// Ask `question`, showing `def` as the default. Returns the trimmed answer,
// or `def` if the user just hit Enter. Returns false if stdin hit EOF (e.g.
// the wizard was run non-interactively with no input) so the caller can
// abort instead of looping forever.
bool ask(const std::string& question, const std::string& def,
         std::string& out) {
    if (def.empty())
        std::printf("%s: ", question.c_str());
    else
        std::printf("%s [%s]: ", question.c_str(), def.c_str());
    std::fflush(stdout);
    std::string line;
    if (!std::getline(std::cin, line)) return false;
    std::string t = trim(line);
    out = t.empty() ? def : t;
    return true;
}

bool ask_yesno(const std::string& question, bool def, bool& out) {
    std::string ans;
    if (!ask(question + (def ? " (Y/n)" : " (y/N)"), def ? "y" : "n", ans))
        return false;
    char c = ans.empty() ? (def ? 'y' : 'n')
                         : static_cast<char>(std::tolower(
                               static_cast<unsigned char>(ans[0])));
    out = (c == 'y');
    return true;
}

bool is_digits(const std::string& s) {
    if (s.empty()) return false;
    for (char c : s)
        if (c < '0' || c > '9') return false;
    return true;
}

// Quote a value for an INI line only if needed. INI values here are literal
// (no escape processing), so a Windows path with backslashes is fine as-is.
void write_ini(std::ostream& os, const std::string& host,
               const std::string& port, const std::string& backlog,
               const std::string& data, bool studio,
               const std::string& http_port, const std::string& http_user) {
    os << "# openads.ini — generated by `openads_serverd --setup`.\n"
       << "# Point the daemon here with:  openads_serverd --config "
          "openads.ini\n"
       << "# Command-line flags given as well override these keys.\n\n"
       << "[server]\n"
       << "host = " << host << "\n"
       << "port = " << port << "\n"
       << "backlog = " << backlog << "\n"
       << "data = " << data << "\n";
    if (studio) {
        os << "http_port = " << http_port << "\n";
        if (!http_user.empty())
            os << "http_user = " << http_user << "\n";
        else
            os << "# http_user = admin:change-me   # console is open\n";
    } else {
        os << "# http_port = 6263   # uncomment to enable the Studio "
              "web console\n";
    }
}

