bool run_setup_wizard(const std::string& exe_path, SetupResult& out) {
    std::printf(
        "\nOpenADS server setup\n"
        "====================\n"
        "Answer a few questions; a config file is written that the server\n"
        "(or a service) reads with --config. Press Enter to accept the\n"
        "[default] shown.\n\n");

    std::string host, port, data, ini_path;
    if (!ask("Bind address (0.0.0.0 = all interfaces)", "0.0.0.0", host))
        return false;

    for (;;) {
        if (!ask("Wire (TCP) port clients connect to", "6262", port))
            return false;
        if (!is_digits(port) || std::stol(port) > 65535) {
            std::printf("  ! port must be a number 0..65535\n");
            continue;
        }
        if (port == "6262")
            std::printf(
                "  note: 6262 is the historical ADS port. If an "
                "Advantage service still\n        runs on this host, pick "
                "another (e.g. 6263) to avoid a bind clash.\n");
        break;
    }

    if (!ask("Data directory (where tables live)", ".", data)) return false;

    bool studio = false;
    std::string http_port, http_user;
    if (!ask_yesno("Enable the Studio web console (the ARC replacement)?",
                   false, studio))
        return false;
    if (studio) {
        for (;;) {
            if (!ask("  Studio HTTP port", "6263", http_port)) return false;
            if (is_digits(http_port) && std::stol(http_port) <= 65535) break;
            std::printf("  ! port must be a number 0..65535\n");
        }
        std::string user;
        if (!ask("  Studio admin user (blank = open console)", "", user))
            return false;
        if (!user.empty()) {
            std::string pass;
            if (!ask("  Studio admin password", "", pass)) return false;
            http_user = user + ":" + pass;
        }
    }

    if (!ask("Write the config to", "openads.ini", ini_path)) return false;

    {
        std::ifstream exists(ini_path);
        if (exists.good()) {
            bool ow = false;
            if (!ask_yesno("  " + ini_path + " exists — overwrite?", false,
                           ow))
                return false;
            if (!ow) {
                std::printf("Aborted; nothing written.\n");
                return false;
            }
        }
    }

    bool autostart = false;
    if (!ask_yesno("Start OpenADS automatically at boot (install a service)?",
                   false, autostart))
        return false;

    std::ofstream f(ini_path, std::ios::binary);
    if (!f) {
        std::printf("error: cannot write %s\n", ini_path.c_str());
        return false;
    }
    write_ini(f, host, port, "16", data, studio, http_port, http_user);
    f.close();
    out.ini_path = ini_path;

    std::printf("\nWrote %s:\n", ini_path.c_str());
    std::printf("  bind        %s:%s\n", host.c_str(), port.c_str());
    std::printf("  data dir    %s\n", data.c_str());
    if (studio)
        std::printf("  Studio      http://%s:%s/%s\n", host.c_str(),
                    http_port.c_str(),
                    http_user.empty() ? " (open)" : " (login required)");
    std::printf("\nRun it now with:\n  openads_serverd --config %s\n",
                ini_path.c_str());

    out.want_service = false;
    if (autostart) {
#if defined(_WIN32)
        out.want_service = true;  // main() registers the Windows Service
#else
        write_posix_service(exe_path, ini_path, data);
#endif
    }
    (void)exe_path;  // unused on Windows
    return true;
}

